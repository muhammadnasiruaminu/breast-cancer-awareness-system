          <h1 class="card-title h2">Types of Breast Cancer</h1>
          
          <p>
          There are several types of breast cancer. The type of breast cancer you have depends
          on where in the breast it started and other factors.
          </p>
          <p>
          There are many types of breast cancer, and many different ways to describe them. It's
          easy to get confused.
          A breast cancer's type is determined by the specific cells in the breast that
          become cancer.
          </p>
          <h3>Ductal or lobular carcinoma</h3>
          <h3>Less common types of breast cancer</h3>
          <br>
          <h5>Ductal or lobular carcinoma</h5>
          <p>
            Most breast cancers are carcinomas, which are tumors that start in the epithelial cells
            that line organs and tissues throughout the body. When carcinomas form in the breast,
            they are usually a more specific type called adenocarcinoma, which starts in cells in
            the ducts (the milk ducts) or the lobules (glands in the breast that make milk).
          </p>

          <h5>In situ vs. invasive breast cancers</h5>
          <p>
          The type of breast cancer can also refer to whether the cancer has spread or not. In
          situ breast cancer (ductal carcinoma in situ or DCIS) is a pre-cancer that starts in a
          milk duct and has not grown into the rest of the breast tissue. The term invasive (or
          infiltrating) breast cancer is used to describe any type of breast cancer that has spread
          (invaded) into the surrounding breast tissue.
          </p>
          <h5>Ductal carcinoma in situ (DCIS)</h5>
          <p>
          Ductal carcinoma in situ (DCIS; also known as intraductal carcinoma) is a non-invasive
          or pre-invasive breast cancer.
          </p>
              
          <h5>Invasive breast cancer (ILC or IDC)</h5>
          <p>
          Invasive (or infiltrating) breast cancer has spread into surrounding breast tissue. The
          most common types are invasive ductal carcinoma and invasive lobular
          carcinoma. Invasive ductal carcinoma makes up about 70-80% of all breast cancers.
          </p>

          <h5>Special types of invasive breast cancers</h5>
          <p>
          Some invasive breast cancers have special features or develop in different ways that
          influence their treatment and outlook. These cancers are less common but can be more
          serious than other types of breast cancer.
          </p>

          <h5>Triple-negative breast cancer</h5>
          <p>
          Triple-negative breast cancer is an aggressive type of invasive breast cancer in which
          the cancer cells don't have estrogen or progesterone receptors (ER or PR) and also
          don't make any or too much of the protein called HER2. (The cells test "negative" on all
          3 tests.) It accounts for about 15% of all breast cancers and can be a difficult cancer to
          treat.
          </p>

          <h5>Inflammatory breast cancer</h5>
          <p>
          Inflammatory breast cancer is an aggressive type of invasive breast cancer in which
          cancer cells block lymph vessels in the skin, causing the breast to look "inflamed." It is
          rare and accounts for about 1% to 5% of all breast cancers.
          </p>

          <h5>Less common types of breast cancer</h5>
          <p>
          There are other types of breast cancers that start to grow in other types of cells in the
          breast. These cancers are much less common, and sometimes need different types of
          treatment.
          </p>

          <h5>Paget disease of the breast</h5>
          <p>
          Paget disease of the breast is rare, accounting for only about 1-3% of all cases of
          breast cancer. It starts in the breast ducts and spreads to the skin of the nipple and
          then to the areola (the dark circle around the nipple).
          </p>

          <h5>Angiosarcoma</h5>
          <p>
          Sarcomas of the breast are rare making up less than 1% of all breast
          cancers. Angiosarcoma starts in cells that line blood vessels or lymph vessels. It can
          involve the breast tissue or the skin of the breast. Some may be related to prior
          radiation therapy in that area.
          </p>

          <h5>Phyllodes tumor</h5>
          <p>
          Phyllodes tumors are rare breast tumors. They develop in the connective tissue
          (stroma) of the breast, in contrast to carcinomas, which develop in the ducts or lobules.
          Most are benign, but there are others that are malignant (cancer).
          </p>


          
