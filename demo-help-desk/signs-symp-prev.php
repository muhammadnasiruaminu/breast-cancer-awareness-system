        <div class="card card-bordered p-4 p-md-7">
          <h1 class="card-title h2">Breast Cancer Risk Factors</h1>
            <p>
              We don't know what causes breast cancer. But we do know there are factors you can control, 
              such as smoking and drinking alcohol, which can increase your risk of developing the disease.
            </p>
            <p>
              Everyone wants to know what they can do to lower their breast cancer risk. Although doctors don't know what causes breast cancer, 
              they do know there are factors linked to a higher-than-average risk of developing the disease. Some factors associated with an increase in breast cancer risk — being a woman, your age, 
              and your genetics, for example — can't be changed. Other factors — lack of exercise, smoking cigarettes, and eating certain foods — can be altered by making lifestyle choices.
            </p>
            <p>
              By choosing the healthiest lifestyle options possible, you can empower yourself and make sure your breast cancer risk is as low as possible.
            </p>

            <h3>Known breast cancer risk factors</h3>
            <p> <font size="4" font color="black"><span ><b><i> > Sex:</i></b></span></font>  Being a woman, or a person assigned female at birth, is one of the most important risk factors for developing breast cancer.</p>

            <p> <font size="4" font color="black"><span ><b><i> > Age:</i></b></span></font> As with many other diseases, your risk of breast cancer goes up as you get older. About two out of three invasive breast cancers are found in women 55 or older.</p>

            <p> <font size="4" font color="black"><span ><b><i> > Family History:</i></b></span></font> Women with close relatives — especially sisters, mothers, or daughters — who have been diagnosed with breast cancer have a higher risk of developing the disease.</p>
        
            <p> <font size="4" font color="black"><span ><b><i> > Genetics:</i></b></span></font> About 5% to 10% of breast cancers are thought to be hereditary, caused by abnormal genes passed from parent to child.</p>

            <p> <font size="4" font color="black"><span ><b><i> > Personal History of Breast Cancer:</i></b></span></font> If you've been diagnosed with breast cancer, you're three to four times more likely to develop a new cancer in the other breast or a different part of the same breast.</p>
        
            <p> <font size="4" font color="black"><span ><b><i> > Radiation to Chest or Face Before Age 30:</i></b></span></font> If you had radiation to the chest or face to treat another cancer or acne, you have a higher-than-average risk of breast cancer.</p>

            <p> <font size="4" font color="black"><span ><b><i> > Certain Breast Changes: </i></b></span></font> If you've been diagnosed with certain benign (not cancer) breast conditions, you may have a higher risk of breast cancer.</p>

            
