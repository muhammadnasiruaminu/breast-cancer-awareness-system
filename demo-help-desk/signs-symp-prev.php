            <h1 class="card-title h2">Signs and Symptoms of Breast Cancer</h1>
            <p>Breast cancer symptoms can range from lumps to swelling to skin changes. <br></p>
            <p>Breast cancer symptoms vary widely, and many breast cancers have no obvious symptoms at all</p>     
        
            <p>
              According to the American Cancer Society, any of the following unusual changes in the breast can be a symptom of breast cancer:
            </p>
        
            <h5> > Swelling of all or part of the breast</h5>

            <h5> > Skin irritation or dimpling</h5>

            <h5> > Breast pain</h5>

            <h5> > Nipple pain or the nipple turning inward</h5>

            <h5> > Redness, scaliness, or thickening of the nipple or breast skin</h5>

            <h5> > A nipple discharge other than breast milk</h5>

            <h5> > A lump in the underarm area</h5>
                    
             <p>These changes also can be signs of less serious conditions, such as an infection or a cyst.</p>

             <h3>Breast pain</h3>
             <p>
                Although pain is not a common symptom of early breast cancer, a tumor can cause pain if it pushes into nearby healthy tissue. 
                Pain and tenderness are often one of the first symptoms of inflammatory breast cancer. 
                This rare type of cancer tends to grow quickly and causes reddening and dimpling of the skin over the breast.
             </p>
              <p>
                A rare form of breast cancer called Paget disease of the nipple can cause pain and burning as early symptoms, along with irritation of the nipple.
              </p>
              <p>
                If you have breast pain that is getting worse without any clear explanation, be sure to see your doctor.
              </p>

              <h3>Breast lumps or masses</h3>
              <p>
                For some people, the first sign of breast cancer is a new lump or mass in the breast that you or your doctor can feel. 
                A lump that is painless, hard, and has uneven edges is more likely to be cancer. But sometimes cancers can be tender, soft, and rounded.
              </p>
              <p>
                In some cases, a lump may be too small for you to feel or to cause any unusual changes you can notice on your own. 
                Often, an abnormal area turns up on a screening mammogram (X-ray of the breast), which leads to additional testing.
              </p>

              <h3>Breast self-exam and screenings</h3>
              <p>
                Performing a monthly breast self-exam is the best way of noticing any changes in your breasts. 
                And it's important to get any breast changes checked out by a doctor as soon as possible.
              </p>
              <p>
               If you're older than 40 or have a high risk of developing breast cancer, you should also have an annual mammogram and a physical exam. 
               The earlier breast cancer is found and diagnosed, the better your chances of successful treatment.
              </p>

