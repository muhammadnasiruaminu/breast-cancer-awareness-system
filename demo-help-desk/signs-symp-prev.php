            <h1 class="card-title h2">Ways to prevent Breast Cancer</h1>
              <br>
            <h5> > Keep Weight in Check</h5>

            <h5> > Be Physically Active</h5>

            <h5> > Eat Your Fruits & Vegetables and Limit Alcohol (Zero is Best)</h5>

            <h5> > Don't Smoke</h5>

            <h5> > Breastfeed, if Possible</h5>

            <h5> > Avoid Birth Control Pills, Particularly After Age 35</h5>

            <h5> > Avoid Hormone Therapy for Menopause</h5>
            <br>
                    
            <h5>Don’t Forget Mammograms</h5>
            <p>Breast cancer screening with mammograms saves lives. It doesn’t help prevent cancer, but it can help find cancer early when it’s more treatable.</p>
            <p>Most women should get yearly mammograms starting at age 40.</p>
            <p>Women at higher risk for breast cancer may need to start mammograms earlier. So, it’s best to talk to a doctor by age 30 about any breast cancer risk factors you may have and if you’d benefit from earlier screening.</p>
            <p>Regularly doing breast self-exams is not recommended for screening. They haven’t been found to have benefit. But you should be familiar with your breasts and tell a healthcare provider right away if you notice any changes in how your breasts look or feel.</p>


